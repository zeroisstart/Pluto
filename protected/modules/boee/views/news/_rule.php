<p>（四）、兑换说明会员积分计划于次年1月1日至1月31日进行兑付（购房类积分每半年兑付一次）。首次积分兑付时间为2013年1月1日至1月31日。我们将向积分在50分以上（含50分）的客户发送兑付通知。通知发送后，会员应在一个月内完成兑付，最晚不得晚于1月31日。如当期不能及时兑付，则其积分自动折半后转入下期。实物奖励方法由于每种礼品的数量有限，请会员在兑付通知发出后30天内与保亿会联系登记选择的礼品类型，先到先得。积分计划实施期满，奖励兑付后的积分清零，未兑付的积分将自动折半结转至下一期。积分不得转让、赠予、买卖。积分兑付最高限额为20000分。超过20000分则按最高额20000分进行兑付。
</p>
