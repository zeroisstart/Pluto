<h1>保亿会积分活动细则</h1>
  <div>
   <p>为更好地为会员提供全方位的服务与优惠，加强保亿置业集团与会员之间的关系，保亿会采取活动积分、购房积分计划，回报关心与支持保亿的广大业主。
</p>
<p>（一）、积分流程<br />
1．申请入会获得保亿会会员卡。<br />
2．通过入会、参与活动、合理化建议、推荐购房、购房等方式获得积分。<br />
3．在活动现场签到，办理积分手续须出示会员卡，经保亿会工作人员或相关人员审核确认。<br />
4．将积分情况录入个人专属积分档案。<br />
5．积分活动期满，通知兑付奖励，领取奖品或奖金。
</p>
<p>（二）、积分计划期限<br />
按公历自然年进行积分，即每年的1月1日-12月31日，保亿会成立首次积分计算期限为2011年9月1日——2012年12月31日。
</p>
<p>（三）、积分方法<br />
会员自申请之日起根据购买房屋自动归属为普通会员及VIP会员，普通会员初始积分为10分,VIP会员初始积分为15分。<br />
随着会员通过参加活动等方式不断累积，会员积分也随之攀升。不论会员级别如何，基本按1分＝1元来获得现金或多项实物及精神的奖励。每年积分排名前10位的普通会员在享受积分兑换的同时，自动升级为VIP会员。
</p>
